<h1 id="Title">The Concise History Of The African Wildlife Foundation</h1>
<img id="AWF" src="Images/African%20Sunset.jpg" alt="African Sunset" width="10%" height="10%">

<div class="y1960">
<h1>1960 - 1980</h1>
    <ul>
        <li>
            1961 - The African Wildlife Foundation is started.
        </li>
        
        <li>
            1963 - AWLF starts giving out scholarships "to enable young africans to study wildlife managment and bioligy" (AWF 1).  
        </li>
        
        <li>
            1968 - AWLF starts the "Give a lion a home" campaign (AWF).
        </li>
        
        <li>
            1978 - AWLF establishes the mountain gorilla project in Rwanda to help protect endangered gorilla species
        </li>
    </ul>
</div>
    
<div class="y1980">
<h1>1980 - 2000</h1>
    <ul>
        <li>
            1988 - AWF launched the "save the elephant" campaign because of a rapid increase of poaching 
        </li>
        
        <li>
            1989 - AWF's landmark "Only Elephants Should Wear Ivory" ad campaign runs on televison and news paper commercials.  
        </li>
        
        <li>
            1998 - AWF launches it's program known ass the "African Hearland Project" that identifies converse and large landscapes and aims to protect them
        </li>
        
        <li>
            2000 - AWF helps form the new Tanzanian Land Convservation Trust to secure important wildlife dispersal areas
        </li>
    </ul>
</div>    
    
   <div class="y2000">
<h1>2000 - 2021</h1>
    <ul>
        <li>
            2009 - The Lomako conservation science center is created so AWF can study <a href="https://en.wikipedia.org/wiki/Bonobo" target="_blank">Bonobos</a>  
        </li>
        
        <li>
            2018 - AWF pledges a USD 25 million investment over four years in a successful attempt to end wild life crimes
        </li>
        
        <li>
            2018 - AWF and Nickelodean International create a mini series called "Together For Good Wild Life Special" 
        </li>
        
        <li>
            2018 - AWF donates 27.8 <a href="https://en.wikipedia.org/wiki/Hectare" target="_blank">hectares</a> of land to the Rwandan Government to expand all sorts of national resources
        </li>
        
        <li>
            2020 - AWF initiates Pan-Africa leadership platforms for civil society orginizations, and to help with covid issues in africa.
        </li>
        
        <li>
            2021 - AWF opens three new classroom African schools in nothern uigana, kidepo, and sarachon. They have rebuild six schoold in africa
        </li>
    </ul>
</div>    
